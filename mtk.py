    def wkbType2String(self, t: QgsWkbTypes.GeometryType):
        if t == QgsWkbTypes.GeometryType.PointGeometry:
            return 'Point'
        elif t == QgsWkbTypes.GeometryType.LineGeometry:
            return 'LineString'
        elif t == QgsWkbTypes.GeometryType.PolygonGeometry:
            return 'Polygon'
        return None

